<section>
    <h2>Benefits of Hatha Yoga</h2>
    <ul>
        <li>
            <strong>Increased Flexibility:</strong> Regular practice of Hatha yoga
            can significantly improve flexibility in the muscles and joints, reducing
            the risk of injury in daily activities.
        </li>
        <li>
            <strong>Strength Building:</strong> Many Hatha poses engage multiple
            muscle groups, contributing to overall strength and endurance.
        </li>
        <li>
            <strong>Stress Reduction:</strong> The combination of breath work, physical
            movement, and meditation helps to lower stress levels and promote relaxation.
        </li>
        <li>
            <strong>Improved Focus and Concentration:</strong> Hatha yoga encourages
            mindfulness and mental clarity, which can enhance focus both on and off
            the mat.
        </li>
        <li>
            <strong>Enhanced Mind-Body Connection:</strong> Practitioners often find
            a greater sense of awareness and connection between their physical body
            and mental state.
        </li>
    </ul>
</section>

<section>
    <h2>What to Expect in a Hatha Yoga Class</h2>
    <p>
        A typical Hatha yoga class is generally slow-paced, allowing for
        in-depth exploration of postures and breath. Classes may begin with a
        centering practice, followed by a series of asanas that flow seamlessly
        together. Each pose is often held for several breaths, allowing for
        adjustments in alignment and technique.
    </p>
    <p>
        Classes may also include pranayama exercises and conclude with a period
        of relaxation or meditation, fostering a sense of calm and
        introspection.
    </p>
</section>

