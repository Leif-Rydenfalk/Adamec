<section>
    <h2>The Origins of Hatha Yoga</h2>
    <p>
        Hatha yoga has its origins in ancient Indian texts, with the word
        "Hatha" translating to "force" in Sanskrit. The practice was designed to
        prepare the body for meditation and spiritual growth, promoting
        strength, flexibility, and a balance of energy. The most significant
        texts associated with Hatha yoga include the "Hatha Yoga Pradipika" and
        the "Gheranda Samhita," which outline various postures, breathing
        techniques, and cleansing practices.
    </p>
</section>

